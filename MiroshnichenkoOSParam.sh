#!/bin/bash
echo -n "Введіть назву параметра пам'яті: "
read mempar
echo $(grep $mempar /proc/meminfo)
echo -e "$mempar - об'єм пам'яті, зайнятий найбільш часто використовуваними сторінками пам'яті, коли ці сторінки пам'яті
активно використовуються процесами і звільнятимуться тільки в разі крайньої необхідності"
echo -n "Введіть назву параметру процесору: "
read cpupar
echo $(grep "$cpupar" /proc/cpuinfo | uniq)
echo "$cpupar - загальна кількість ядер у процесора"
